#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *new;

	if (lst && lst->content)
	{
		new = lst;
		while (new)
		{
			f(new);
			new = new->next;
		}
	}
}
