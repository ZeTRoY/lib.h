char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*str;

	i = 0;
	str = (char *)s;
	if (s != 0)
	{
		while (str[i])
			if (str[i++] == (char)c)
				return (&str[i - 1]);
		if (str[i] == (char)c)
			return (&str[i]);
	}
	return (0);
}
