#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int i;

	i = 0;
	if (s1 != 0 && s2 != 0)
	{
		while (s1[i] && s2[i])
		{
			if (s1[i] != s2[i])
				return (s1[i] - s2[i]);
			i++;
		}
		if ((s1[i] && s2[i] == '\0') || (s1[i] == '\0' && s2[i]))
			return (s1[i] - s2[i]);
	}
	else
	{
		if (s1 == 0)
			return (s2[0]);
		else
			return (s1[0]);
	}
	return (0);
}
