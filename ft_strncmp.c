#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int i;

	i = 0;
	if (s1 && s2)
	{
		while (s1[i] && s2[i] && i != (int)n)
		{
			if (s1[i] != s2[i])
				return (s1[i] - s2[i]);
			i++;
		}
		if ((s1[i] == '\0' || s2[i] == '\0') && i != (int)n)
		{
			if (s1[i] == '\0')
				return (s2[i]);
			else
				return (s1[i]);
		}
	}
	return (0);
}
